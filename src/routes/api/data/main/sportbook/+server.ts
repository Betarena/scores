export async function GET
(
  req: any
): Promise < unknown >
{
  try
  {
    // ### NOTE:
    // ### handle url-query data
    const geoPos: string = req?.url?.searchParams?.get('geoPos');
    const all: string =	req?.url?.searchParams?.get('all');
    const hasura: string = req?.url?.searchParams?.get('hasura');

    let data: unknown;
    let loadType: string = "‚ö°Ô∏è Redis (cache)";

    // ### NOTE:
    // ### gathers Sportbook (Main) widget main data.
    // ### NOTE:
    // ### contains üü¶ Hasura (PostgreSQL) fallback.
    const if_M_0: boolean =
      all != null
      && geoPos != null
    ;
    if (if_M_0)
    {
      // ### CHECK | IMPORTANT
      // ### for existance in cache.
      if (!hasura)
      {
        data = await get_target_hset_cache_data
        (
          SPD_C_D_A1,
          geoPos
        );
        if (data == undefined)
        {
          data = await get_target_hset_cache_data
          (
            SPD_C_D_A1,
            'en'
          );
        }
      }

      // ### CHECK | IMPORTANT
      // ### for default in Hasura.
      if (!data || hasura)
      {
        data = await fallbackMainData
        (
          geoPos
        );
        loadType = 'üü¶ Hasura (SQL)';
      }

      // ### [üêû]
      // console.log(`üìå loaded [FPROB] with: ${loadType}`);

      if (data != null)
      {
        const compressed: string = LZString.compress(JSON.stringify(data));

        // ### [üêû]
        // console.log(JSON.parse(LZString.decompress(compressed)));

        return json
        (
          {
            data: compressed,
            loadType
          }
        );
      }
    }

    // ### NOTE:
    // ### gathers Sportbook (All) widget main data.
    // ### NOTE:
    // ### contains üü¶ Hasura (PostgreSQL) fallback.
    const if_M_1: boolean =
      all == null
      && geoPos != null
    ;
    if (if_M_1)
    {
      // ### CHECK | IMPORTANT
      // ### for existance in cache.
      if (!hasura)
      {
        data = await get_target_hset_cache_data
        (
          SPD_C_D_A,
          geoPos
        );
        if (data == undefined)
        {
          data = await get_target_hset_cache_data
          (
            SPD_C_D_A,
            'en'
          );
        }
      }

      // ### CHECK | IMPORTANT
      // ### for default in Hasura.
      if (!data || hasura)
      {
        data = await fallbackMainData_1
        (
          geoPos
        );
        loadType = 'üü¶ Hasura (SQL)';
      }

      // ### [üêû]
      // console.log(`üìå loaded [FPROB] with: ${loadType}`)

      if (data != null)
      {
        const compressed: string = LZString.compress(JSON.stringify(data));

        // ### [üêû]
        // console.log(JSON.parse(LZString.decompress(compressed)));

        return json
        (
          {
            data: compressed,
            loadType
          }
        );
      }
    }

    // ### IMPORTANT
    return json
    (
      null
    );
  }
  catch (ex)
  {
    console.error
    (
      '‚ùå Err: ',
      ex
    );
    return json
    (
      null,
      {
        status: 400,
        statusText: 'Uh-oh! There has been an error'
      }
    );
  }
}

