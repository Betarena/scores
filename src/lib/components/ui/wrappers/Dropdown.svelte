  // │ 1. function (..)                                                       │
  // │ 2. async function (..)                                                 │
  // ╰────────────────────────────────────────────────────────────────────────╯

  function showPopup() {
    if (!show) {
      show = true;
      dispatch("show");
    } else {
      hide();
    }
  }

  function hide() {
    if (show) {
      show = false;
      dispatch("hide");
    }
  }

  // #endregion ➤ 🛠️ METHODS
</script>

