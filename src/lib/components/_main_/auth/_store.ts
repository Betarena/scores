/**
 * @description
 *  üì£
 * @return
 */
function createLocalStore
(
)
{
  const
    {
      subscribe,
      set,
      update
    } = writable
    (
      storeObject
    ),
    /**
     * @description
     *  üì£ Complementary 'store' added methods.
     */
    methods
      = {

        // ‚ï≠‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚ïÆ
        // ‚îÇ üì£ Main Logic                                                                    ‚îÇ
        // ‚ï∞‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚ïØ

        /**
         * @author
         *  @migbash
         * @summary
         *  - üîπ HELPER
         *  - IMPORTANT
         * @description
         *  üì£ Update **target** `list` data of target `properties` to update.
         * @param { [IDataProp, any][] } data
         *  üí† **[required]** Target data to update.
         * @return { void }
         */
        updateData:
        (
          data: [IDataProp, any][]
        ): void =>
        {
          for (const iterator of data)
          {
            const
              dataTarget = iterator[0],
              dataPoint = iterator[1]
            ;

            if (dataTarget == 'globalStateAdd')
            {
              if (dataPoint == 'ExistingEmailLoginSent')
              {
                methods.startEmailResendCountdown();
                storeObject.globalState.delete('AuthenticationStart');
              }
              else if (dataPoint == 'NewEmailRegisterationSent')
              {
                storeObject.globalState.delete('AuthenticationStart');
              }
              storeObject.globalState.add(dataPoint);
            }
            else if (dataTarget == 'globalStateRemove')
            {
              storeObject.globalState.delete(dataPoint);
            }
            else if (dataTarget == 'globalSateErrorAdd')
            {
              storeObject.globalStateErrors.add(dataPoint);
            }
            else if (dataTarget == 'globalStateErrorRemove')
            {
              storeObject.globalStateErrors.delete(dataPoint);
            }
            else if (dataTarget == 'resendEmailCountdown')
            {
              storeObject.resendEmailCountdown = dataPoint;
            }
          }

          set
          (
            storeObject
          );

          return;
        },

        /**
         * @author
         *  @migbash
         * @summary
         *  üü¶ HELPER
         * @description
         *  üìù Kickstarts email verification `countdown`.
         * @return { void }
         */
        startEmailResendCountdown:
        (
        ): void =>
        {
          const
            futureDate = new Date()
          ;

          // ‚ï≠‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
          // ‚îÇ NOTE: :|: add 5 min.
          // ‚ï∞‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
          futureDate.setMinutes
          (
            futureDate.getMinutes() + 5
          );

          const
            interval = setInterval
            (
              (
              ) =>
              {
                const
                  dateObjDif = breakdownDates(futureDate.getTime() - new Date().getTime());
                ;

                // [üêû]
                // console.log('dateObjDif', dateObjDif);

                methods.updateData
                (
                  [
                    ['resendEmailCountdown', dateObjDif]
                  ]
                );

                if (dateObjDif[0] <= 0 && dateObjDif[1] <= 0)
                {
                  clearInterval(interval);
                  methods.updateData
                  (
                    [
                      ['globalStateAdd', 'AllowResendEmailLogin']
                    ]
                  );
                }

                return;
              },
              1000
            )
          ;

          return;
        }

      }
  ;

  return {
    subscribe,
    set,
    update,
    ...methods
  };
}

// #endregion ‚û§ üõ†Ô∏è METHODS

export const scoresAuthStore = createLocalStore();
