// #region ‚û§ üìå VARIABLES

const
  storeObject: IAuthWidget
    = {
      globalState: new Set(),
      globalStateErrors: new Set(),
      resendEmailCountdown: null,
    }
;

type IDataProp =
  | 'globalStateAdd'
  | 'globalStateRemove'
  | 'globalSateErrorAdd'
  | 'globalStateErrorRemove'
  | 'resendEmailCountdown'
;

